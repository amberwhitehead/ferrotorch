// --- StepLR -----------------------------------------------------------------

#[test]
fn step_lr_matches_reference() {
    let fixtures = load_fixtures();
    let cases: Vec<_> = fixtures
        .fixtures
        .iter()
        .filter(|f| f.scheduler == "StepLR")
        .collect();
    assert!(!cases.is_empty(), "no StepLR fixtures found");

    for case in cases {
        let p = &case.params;
        let base_lr = p["base_lr"].as_f64().unwrap();
        let step_size = p["step_size"].as_u64().unwrap() as usize;
        let gamma = p["gamma"].as_f64().unwrap();

        let mut sched = StepLR::new(base_lr, step_size, gamma);
        let mut opt = MockOptimizer::new(base_lr);
        let mut actual = Vec::with_capacity(case.n_steps);

        for _ in 0..case.n_steps {
            sched.step(&mut opt);
            actual.push(opt.lr);
        }

        let label = format!("StepLR(base_lr={base_lr}, step_size={step_size}, gamma={gamma})");
        assert_lr_sequence(&label, &actual, &case.lr_sequence);

        // Also verify get_lr() agrees with the optimizer's lr after the final step.
        // This exercises LrScheduler::get_lr and StepLR::get_lr.
        assert!(
            (sched.get_lr() - opt.lr).abs() < LR_TOL,
            "{label}: sched.get_lr()={} != opt.lr={}",
            sched.get_lr(),
            opt.lr
        );
    }
}

