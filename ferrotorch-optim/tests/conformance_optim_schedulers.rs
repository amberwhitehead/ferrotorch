// ---------------------------------------------------------------------------
// Scheduler count smoke test — ensures all 12 scheduler kinds are covered
// ---------------------------------------------------------------------------

#[test]
fn all_scheduler_kinds_present_in_fixtures() {
    let fixtures = load_fixtures();
    let expected_schedulers = [
        "StepLR",
        "MultiStepLR",
        "ExponentialLR",
        "CosineAnnealingLR",
        "CosineAnnealingWarmRestarts",
        "CyclicLR",
        "OneCycleLR",
        "PolynomialLR",
        "ConstantLR",
        "LinearLR",
        "LinearWarmup",
        "ReduceLROnPlateau",
    ];

    let present: std::collections::HashSet<&str> = fixtures
        .fixtures
        .iter()
        .map(|f| f.scheduler.as_str())
        .collect();

    let missing: Vec<&&str> = expected_schedulers
        .iter()
        .filter(|s| !present.contains(**s))
        .collect();

    assert!(
        missing.is_empty(),
        "Missing scheduler fixtures: {missing:?}"
    );

    // Each scheduler must have at least 2 fixture variants.
    for sched_name in &expected_schedulers {
        let count = fixtures
            .fixtures
            .iter()
            .filter(|f| f.scheduler == *sched_name)
            .count();
        assert!(
            count >= 2,
            "Scheduler {sched_name} has only {count} fixture variant(s), need >= 2"
        );
    }
}
