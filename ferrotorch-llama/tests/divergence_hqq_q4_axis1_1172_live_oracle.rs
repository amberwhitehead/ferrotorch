//! Live-oracle divergence probes for the HQQ axis=1 grouped Q4 dequantizer
//! (#1172). Generated against `mobiusml/hqq` v0.2.1 + torch via
//! `Quantizer.quantize(nbits=4, axis=1, bitpack=True)` /
//! `Quantizer.dequantize`. R-CHAR-3: every `expected` array is the live HQQ
//! reference dequant output, traced to the reference, not the ferrotorch impl.
//!
//! These exercise the cases the in-crate oracle tests omit: production group
//! sizes 64/128 with multiple groups per row, and a split-half packing case
//! where the two group-axis halves are deliberately distinct (so an
//! adjacent-pair unpack would visibly diverge from split-half).

#![allow(
    clippy::excessive_precision,
    reason = "oracle expected values copied verbatim from the live mobiusml/hqq v0.2.1 + torch reference; full precision is intentional (rounds to dtype at compile time)"
)]

use ferrotorch_llama::{HqqQ4Axis1, dequantize_hqq_q4_axis1};

/// Divergence probe: ferrotorch `dequantize_hqq_q4_axis1` vs the live
/// `mobiusml/hqq` v0.2.1 reference. Pack/unpack: `bitpack.py:24-38`
/// (split-half). Dequant: `quantize.py:179-194`
/// `((W_r - zero) * scale).reshape(shape)`. Inputs and `expected` are the
/// live HQQ reference quantize/dequantize output (R-CHAR-3), not copied
/// from the ferrotorch side.
/// Case `split_half_distinct_halves`: shape=[2, 8], group_size=8.
#[test]
fn divergence_hqq_q4_axis1_split_half_distinct_halves() {
    let w_q: Vec<u8> = vec![15u8, 45u8, 75u8, 105u8, 150u8, 180u8, 210u8, 240u8];
    let scale: Vec<f32> = vec![0.46666663885116577f32, 0.46666663885116577f32];
    let zero: Vec<f32> = vec![-0.0f32, -0.0f32];
    let tile = HqqQ4Axis1::new(w_q, scale, zero, 8, 2, 8);
    let out = dequantize_hqq_q4_axis1(&tile).expect("dequant should succeed");
    let expected: Vec<f32> = vec![
        0.0f32,
        0.9333332777023315f32,
        1.866666555404663f32,
        2.799999713897705f32,
        4.199999809265137f32,
        5.133333206176758f32,
        6.066666126251221f32,
        6.999999523162842f32,
        6.999999523162842f32,
        6.066666126251221f32,
        5.133333206176758f32,
        4.199999809265137f32,
        2.799999713897705f32,
        1.866666555404663f32,
        0.9333332777023315f32,
        0.0f32,
    ];
    assert_eq!(out.len(), expected.len(), "length mismatch");
    for (i, (g, e)) in out.iter().zip(expected.iter()).enumerate() {
        assert!(
            (g - e).abs() < 1e-4f32,
            "split_half_distinct_halves i={i}: ferrotorch got {g}, hqq reference wants {e}"
        );
    }
}

